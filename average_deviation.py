# Accept inputs
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Calculate average
avg = (num1 + num2) / 2

# Calculate deviation
dev1 = num1 - avg
dev2 = num2 - avg

# Print results
print("Average:", avg)
print("Deviation of first number:", dev1)
print("Deviation of second number:", dev2)
