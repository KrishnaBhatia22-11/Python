# 1. String Operations
string1 = "Amazing"
print("Complete String:", string1)
print("First character:", string1[0])
print("Characters from 3rd to 5th:", string1[2:5])
print("String starting from 3rd character:", string1[2:])
print("String two times:", string1 * 2)
print("Concatenate 'Test' with 'Amazing':", "Test" + string1)
print("String[-7:-3]:", string1[-7:-3])
